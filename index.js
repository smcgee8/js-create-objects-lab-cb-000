'use strict';

function Dog(name, breed, age) {
  this.name = name;
  this.breed = breed;
  this.age = age;
}

function Cat(name, breed, age) {
  this.name = name;
  this.breed = breed;
  this.age = age;
}

function Ferret(name, age, foodRequirements) {
  this.name = name;
  this.age = age;
  this.foodRequirements = foodRequirements;
}
